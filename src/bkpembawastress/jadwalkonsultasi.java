    private void setupTable() {
        tableModel = new DefaultTableModel(
            new String[] {
                "tanggal_konsultasi",
                "jam_konsultasi",
                "tempat_konsultasi",
                "status_jadwal_konsultasi",
                "id_permintaan_konsultasi",
                "id_konselor"
            }, 0
        );
        jTable1.setModel(tableModel);
        jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    private void setupCombos() {
        jComboBoxStatus.setEditable(true);
        jComboBox1.setEditable(true);
        jComboBox2.setEditable(true);
        reloadCombos();
    }

    private void reloadCombos() {
        DbUtil.fillCombo(jComboBox1, "Pilih", "SELECT DISTINCT id_permintaan_konsultasi FROM jadwal_konsultasi ORDER BY id_permintaan_konsultasi");
        DbUtil.fillCombo(jComboBox2, "Pilih", "SELECT DISTINCT nomor_induk_pegawai FROM konselor_bk ORDER BY nomor_induk_pegawai");
    }

    private void setupSelection() {
        jTable1.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                fillFormFromSelectedRow();
            }
        });
    }

    private void setupJamOtomatis() {
        updateJamOtomatis();
    }

    private void updateJamOtomatis() {
        jTextField2.setText(new SimpleDateFormat("HH:mm").format(new Date()));
    }

    private void fillFormFromSelectedRow() {
        int row = jTable1.getSelectedRow();
        if (row < 0) {
            return;
        }
        setTanggalValue(String.valueOf(tableModel.getValueAt(row, 0)));
        jTextField2.setText(String.valueOf(tableModel.getValueAt(row, 1)));
        jTextField3.setText(String.valueOf(tableModel.getValueAt(row, 2)));
        jComboBoxStatus.setSelectedItem(String.valueOf(tableModel.getValueAt(row, 3)));
        jComboBox1.setSelectedItem(String.valueOf(tableModel.getValueAt(row, 4)));
        jComboBox2.setSelectedItem(String.valueOf(tableModel.getValueAt(row, 5)));
    }

    private boolean validateInputs() {
        if (getTanggalValue().isEmpty()
                || jTextField2.getText().trim().isEmpty()
                || jTextField3.getText().trim().isEmpty()
                || invalidCombo(jComboBoxStatus)
                || invalidCombo(jComboBox1)
                || invalidCombo(jComboBox2)) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi.", "Jadwal Konsultasi", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }

    private boolean invalidCombo(javax.swing.JComboBox<String> combo) {
        String value = getComboValue(combo);
        return value.isEmpty() || "Pilih".equalsIgnoreCase(value);
    }

    private String getComboValue(javax.swing.JComboBox<String> combo) {
        Object value = combo.getSelectedItem();
        return value == null ? "" : value.toString().trim();
    }

    private void clearForm() {
        jDateChooser1.setDate(null);
        updateJamOtomatis();
        jTextField3.setText("");
        jComboBoxStatus.setSelectedIndex(0);
        jComboBox1.setSelectedIndex(0);
        jComboBox2.setSelectedIndex(0);
        jTable1.clearSelection();
    }

    private void loadTableData() {
        tableModel.setRowCount(0);
        String sql = "SELECT tanggal_konsultasi, jam_konsultasi, tempat_konsultasi, status_jadwal_konsultasi, id_permintaan_konsultasi, id_konselor FROM jadwal_konsultasi";
        try (Connection conn = Db.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                tableModel.addRow(new Object[] {
                    rs.getString("tanggal_konsultasi"),
                    rs.getString("jam_konsultasi"),
                    rs.getString("tempat_konsultasi"),
                    rs.getString("status_jadwal_konsultasi"),
                    rs.getString("id_permintaan_konsultasi"),
                    rs.getString("id_konselor")
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage(), "Jadwal Konsultasi", JOptionPane.ERROR_MESSAGE);
        }
    }

    private String getTanggalValue() {
        Date date = jDateChooser1.getDate();
        if (date == null) {
            return "";
        }
        return new SimpleDateFormat("yyyy-MM-dd").format(date);
    }

    private void setTanggalValue(String value) {
        if (value == null || value.trim().isEmpty()) {
            jDateChooser1.setDate(null);
            return;
        }
        try {
            Date parsed = new SimpleDateFormat("yyyy-MM-dd").parse(value.trim());
            jDateChooser1.setDate(parsed);
        } catch (ParseException e) {
            jDateChooser1.setDate(null);
        }
    }
