    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus.", "Jadwal Konsultasi", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Hapus data terpilih?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            String oldTanggal = String.valueOf(tableModel.getValueAt(row, 0));
            String oldIdPermintaan = String.valueOf(tableModel.getValueAt(row, 4));
            String sql = "DELETE FROM jadwal_konsultasi WHERE id_permintaan_konsultasi = ? AND tanggal_konsultasi = ?";
            try (Connection conn = Db.getConnection();
                 PreparedStatement ps = conn.prepareStatement(sql)) {
                ps.setString(1, oldIdPermintaan);
                ps.setString(2, oldTanggal);
                ps.executeUpdate();
                loadTableData();
                reloadCombos();
                clearForm();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage(), "Jadwal Konsultasi", JOptionPane.ERROR_MESSAGE);
            }
        }    }//GEN-LAST:event_jButton2ActionPerformed

