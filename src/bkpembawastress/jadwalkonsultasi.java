    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diedit.", "Jadwal Konsultasi", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!validateInputs()) {
            return;
        }
        String oldTanggal = String.valueOf(tableModel.getValueAt(row, 0));
        String oldIdPermintaan = String.valueOf(tableModel.getValueAt(row, 4));
        String sql = "UPDATE jadwal_konsultasi SET tanggal_konsultasi = ?, jam_konsultasi = ?, tempat_konsultasi = ?, status_jadwal_konsultasi = ?, id_permintaan_konsultasi = ?, id_konselor = ? WHERE id_permintaan_konsultasi = ? AND tanggal_konsultasi = ?";
        try (Connection conn = Db.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, getTanggalValue());
            ps.setString(2, jTextField2.getText().trim());
            ps.setString(3, jTextField3.getText().trim());
            ps.setString(4, getComboValue(jComboBoxStatus));
            ps.setString(5, getComboValue(jComboBox1));
            ps.setString(6, getComboValue(jComboBox2));
            ps.setString(7, oldIdPermintaan);
            ps.setString(8, oldTanggal);
            ps.executeUpdate();
            loadTableData();
            reloadCombos();
            clearForm();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mengedit data: " + e.getMessage(), "Jadwal Konsultasi", JOptionPane.ERROR_MESSAGE);
        }    }//GEN-LAST:event_jButton4ActionPerformed

