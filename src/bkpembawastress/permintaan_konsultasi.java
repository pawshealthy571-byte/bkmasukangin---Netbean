    private void setupTable() {
        tableModel = new DefaultTableModel(
            new String[] {
                "tanggal_permintaan_konsultasi",
                "topik_permasalahan",
                "sumber_pengajuan",
                "status_permintaan_konsultasi",
                "id_siswa",
                "id_konselor",
                "id_wali_kelas"
            }, 0
        );
        jTable1.setModel(tableModel);
        jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    private void setupCombos() {
        jComboBox1.setEditable(true);
        jComboBox2.setEditable(true);
        jComboBox3.setEditable(true);
        reloadCombos();
    }

    private void reloadCombos() {
        DbUtil.fillCombo(jComboBox1, "Pilih", "SELECT DISTINCT nomor_induk_siswa FROM siswa ORDER BY nomor_induk_siswa");
        DbUtil.fillCombo(jComboBox2, "Pilih", "SELECT DISTINCT nomor_induk_pegawai FROM konselor_bk ORDER BY nomor_induk_pegawai");
        DbUtil.fillCombo(jComboBox3, "Pilih", "SELECT DISTINCT nomor_induk_pegawai FROM wali_kelas ORDER BY nomor_induk_pegawai");
    }

    private void setupSelection() {
        jTable1.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                fillFormFromSelectedRow();
            }
        });
    }

    private void fillFormFromSelectedRow() {
        int row = jTable1.getSelectedRow();
        if (row < 0) {
            return;
        }
        setTanggalValue(String.valueOf(tableModel.getValueAt(row, 0)));
        jTextField2.setText(String.valueOf(tableModel.getValueAt(row, 1)));
        jTextField3.setText(String.valueOf(tableModel.getValueAt(row, 2)));
        jTextField4.setText(String.valueOf(tableModel.getValueAt(row, 3)));
        jComboBox1.setSelectedItem(String.valueOf(tableModel.getValueAt(row, 4)));
        jComboBox2.setSelectedItem(String.valueOf(tableModel.getValueAt(row, 5)));
        jComboBox3.setSelectedItem(String.valueOf(tableModel.getValueAt(row, 6)));
    }

    private boolean validateInputs() {
        if (getTanggalValue().isEmpty()
                || jTextField2.getText().trim().isEmpty()
                || jTextField3.getText().trim().isEmpty()
                || jTextField4.getText().trim().isEmpty()
                || invalidCombo(jComboBox1)
                || invalidCombo(jComboBox2)
                || invalidCombo(jComboBox3)) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi.", "Permintaan Konsultasi", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }

    private boolean invalidCombo(javax.swing.JComboBox<String> combo) {
        String value = getComboValue(combo);
        return value.isEmpty() || "Pilih".equalsIgnoreCase(value);
    }

    private String getComboValue(javax.swing.JComboBox<String> combo) {
        Object value = combo.getSelectedItem();
        return value == null ? "" : value.toString().trim();
    }

    private void clearForm() {
        jDateChooser1.setDate(null);
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jComboBox1.setSelectedIndex(0);
        jComboBox2.setSelectedIndex(0);
        jComboBox3.setSelectedIndex(0);
        jTable1.clearSelection();
    }

    private void loadTableData() {
        tableModel.setRowCount(0);
        String sql = "SELECT tanggal_permintaan_konsultasi, topik_permasalahan, sumber_pengajuan, status_permintaan_konsultasi, id_siswa, id_konselor, id_wali_kelas FROM permintaan_konsultasi";
        try (Connection conn = Db.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                tableModel.addRow(new Object[] {
                    rs.getString("tanggal_permintaan_konsultasi"),
                    rs.getString("topik_permasalahan"),
                    rs.getString("sumber_pengajuan"),
                    rs.getString("status_permintaan_konsultasi"),
                    rs.getString("id_siswa"),
                    rs.getString("id_konselor"),
                    rs.getString("id_wali_kelas")
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage(), "Permintaan Konsultasi", JOptionPane.ERROR_MESSAGE);
        }
    }

    private String getTanggalValue() {
        Date date = jDateChooser1.getDate();
        if (date == null) {
            return "";
        }
        return new SimpleDateFormat("yyyy-MM-dd").format(date);
    }

    private void setTanggalValue(String value) {
        if (value == null || value.trim().isEmpty()) {
            jDateChooser1.setDate(null);
            return;
        }
        try {
            Date parsed = new SimpleDateFormat("yyyy-MM-dd").parse(value.trim());
            jDateChooser1.setDate(parsed);
        } catch (ParseException e) {
            jDateChooser1.setDate(null);
        }
    }
